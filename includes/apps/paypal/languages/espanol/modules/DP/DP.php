module_dp_introduction = <ul>
  <li>Pago directo le permite diseñar y alojar sus propias páginas de pago.</li>
  <li>Mantiene a sus clientes en su sitio en lugar de que paguen en PayPal; PayPal permanece invisible.</li>
</ul>

<p>El pago directo permite a los compradores pagar con tarjeta de crédito o débito durante el proceso de pago. Usted tiene control total sobre la experiencia; sin embargo, debe considerar el cumplimiento del éstandar de seguridad PCI.</p>
<p>Tenga en cuenta que el pago directo no es un producto independiente: debe habilitar y usar Express Checkout junto con Direct Payment.</p>

